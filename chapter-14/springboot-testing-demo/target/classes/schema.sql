DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS TODOS;
CREATE TABLE USERS
(
    ID       int          NOT NULL AUTO_INCREMENT,
    EMAIL    varchar(50)  NOT NULL,
    NAME     LONGTEXT DEFAULT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE TODOS
(
    ID   int          NOT NULL AUTO_INCREMENT,
    TEXT varchar(200) NOT NULL,
    DONE boolean      NOT NULL,
    PRIMARY KEY (ID)
);
